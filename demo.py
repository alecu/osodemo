font = []

def buildfont(chars):
    for line in chars.split("\n"):
        line = line.strip()
        if line:
            line = line.replace(".", "0").replace("*", "1")
            font.append(int(line,2))

def display(line):
    for y in range(8):
        for c in line:
            n = (ord(c) - 48)
            b = font[n*8+y]
            c = format(b, '05b').replace("0", " ").replace("1", "*")
            print(c + " ", end="")
        print()
    print()

